package www.wjl.com.Interview.集合;

/*
 * @desc 链表
 * @author wjl
 * @date 2018/6/12 0012
 */
public class Link {
    private int id;
    private String name;
    private Link next;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Link getNext() {
        return next;
    }

    public void setNext(Link next) {
        this.next = next;
    }
}
